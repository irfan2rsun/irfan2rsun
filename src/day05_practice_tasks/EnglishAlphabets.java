package day05_practice_tasks;

public class EnglishAlphabets {

    public static void main(String[] args) {

        char englishAlphabets = 'A';

        for (englishAlphabets = 'A'; englishAlphabets <= 'Z'; englishAlphabets++){

            System.out.print(englishAlphabets + " ");

        }

    }

}

