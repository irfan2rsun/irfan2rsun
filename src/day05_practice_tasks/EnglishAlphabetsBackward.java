package day05_practice_tasks;

public class EnglishAlphabetsBackward {

    public static void main(String[] args){

        char englishAlphabets;

        for (englishAlphabets = 'Z'; englishAlphabets >= 'A'; englishAlphabets--){

            System.out.print(englishAlphabets + " ");

        }

    }

}

